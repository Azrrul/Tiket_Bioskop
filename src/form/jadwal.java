    public void showtablejadwal()throws SQLException{
        DefaultTableModel tbljdw = new DefaultTableModel(new String[]{
            "kode jadwal", "nama film", "jam tayang", "tanggal tayang", "ruang"
        },0);
        tbljdw.setRowCount(0);
        for (JadwalFilm jdw : this.transaksi.getDataJadwal()) {
            tbljdw.addRow(new String[]{
                jdw.getKodeTayang().toString(),
                jdw.getfilm().getNama_Film(),
                jdw.getjam(),
                jdw.gettgl().toString(),
                jdw.getruang()
            });
            this.tablejadwal.setModel(tbljdw);
        }
    }
    public void showcomboboxfilm()throws SQLException{
        DefaultComboBoxModel cbfilm = new DefaultComboBoxModel();
        for (film film : this.transaksi.get_film() ) {
            cbfilm.addElement(film.getNama_Film());
        }
        this.pilihfilmkode.setModel(cbfilm);
    }

