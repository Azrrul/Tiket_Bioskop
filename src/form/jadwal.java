    private void insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertActionPerformed
        // TODO add your handling code here:
        JadwalFilm jad = new JadwalFilm();
        try {
            jad.setKodeTayang(Integer.parseInt(kodejadwal.getText()));
            jad.setfilm(transaksi.get_film().get(pilihfilmkode.getSelectedIndex()));
            jad.setjamtayang(jamtayang.getText());
            jad.settanggal(new SimpleDateFormat("dd/MM/yyyy").parse(tgltayang.getText()));
            jad.setruang(ruang.getText());
            this.transaksi.input_jadwal(jad);
            this.showcomboboxfilm();
            this.showtablejadwal();
        }catch(SQLException | ParseException err){
            System.out.println(err);
        }
    }//GEN-LAST:event_insertActionPerformed

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tablejadwal.getModel();
        Integer kode = tablejadwal.getSelectedRow();
        kodejadwal.setText(tablejadwal.getValueAt(kode, 0).toString());
        
        try {
                this.transaksi.delete_jadwal(Integer.parseInt(kodejadwal.getText()));
                this.showtablejadwal();
        } catch (SQLException ex) {
  
            System.out.println(ex);
            Logger.getLogger(Movies.class.getName()).log(Level.SEVERE,null,ex);
            JOptionPane.showMessageDialog(null,"salah bro coba di cek lagi");
        }
    }//GEN-LAST:event_DeleteActionPerformed

